    <h4>Perpustakaan ITS</h4>
    Perpustakaan ITS bermula dari Perpustakaan Yayasan Perguruan Tinggi Teknik 10 November (YPTT), yang didirikan pada tahun 
    1959 dan berlokasi di Jl. Embong Ploso 12 Surabaya. Koleksi perpustakaan yang diperoleh saat itu adalah Sumbangan dari 
    penasehat YPPT atas nama Prof. A.G. Pringgodigdo, Seorang professor wanita dari Amerika Serikat, United State Operation 
    Mission (USOM) dan yang terakhir dari PT Shell. Pada awal pendirian Perpustakaan ini pegawainya hanya 2 orang.

    <p>
    Perpustakaan ITS diresmikan tahun 1960 bersamaan dengan diresmikannya ITS menjadi Perguruan Tinggi Negeri. Pada saat itu 
    jumlah pegawai bertambah menjadi 5 orang yang terdiri dari 3 orang wanita dan 2 orang pria. Pada tahun 1966 Perpustakaan 
    ITS pindah ke Jalan Baliwerti 119-121 Surabaya, dan menempati ruang seluas 220 m2 dengan jumlah pegawai menjadi 8 orang 
    yang terdiri dari 4 wanita dan 4 pria. Pada tahun 1967, perpustakaan mendapat tambahan seorang pegawai baru lulusan 
    Akademi Perpustakaan sehingga pegawainya menjadi 9 orang. Pada tahun 1975 Perpustakaan ITS pindah ke jalan Cokroaminoto 
    12A Surabaya, dan menempati ruang seluas 300 m2. Pada tahun 1982 Perpustakaan ITS pindah kampus ITS Sukolilo dan 
    menempati 1707 m2. Pada bulan Maret tepatnya tahun1995, Perpustakaan ITS menempati gedung seluas 7.500 m2 yang terdiri 
    5 lantai.
